/**
 * Get gradient by name
 * @param {string} name - Gradient name
 * @returns {object} Gradient object
 */
export const getGradient = (name) => {
  return gradients[name] || gradients.emeraldFinance;
};

/**
 * Get gradients by category
 * @param {string} category - Category name
 * @returns {array} Array of gradient objects
 */
export const getGradientsByCategory = (category) => {
  return Object.entries(gradients)
    .filter(([_, gradient]) => gradient.category === category)
    .map(([key, gradient]) => ({ key, ...gradient }));
};

/**
 * Get all gradient names
 * @returns {array} Array of gradient names
 */
export const getAllGradientNames = () => {
  return Object.keys(gradients);
};

/**
 * Get CSS string for gradient with optional glassmorphism
 * @param {string} name - Gradient name
 * @param {boolean} withGlass - Apply glassmorphism effect
 * @returns {string} CSS styles
 */
export const getGradientCSS = (name, withGlass = false) => {
  const gradient = getGradient(name);
  
  if (withGlass && gradient.glassmorphism) {
    return {
      background: gradient.gradient,
      backdropFilter: gradient.backdropFilter,
      position: 'relative',
      '&::before': {
        content: '""',
        position: 'absolute',
        top: 0,
        left: 0,
        right: 0,
        bottom: 0,
        background: gradient.overlay,
        pointerEvents: 'none'
      }
    };
  }
  
  return {
    background: gradient.gradient
  };
};

/**
 * Get gradient for Tailwind class
 * @param {string} name - Gradient name
 * @returns {string} Gradient CSS value
 */
export const getTailwindGradient = (name) => {
  const gradient = getGradient(name);
  return gradient.gradient;
};

